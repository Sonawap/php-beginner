<?php

$color = "blueyellow";

switch ($color) {
    case 'red':
        echo "The color is red";
        break;

    case 'blue':
        echo "The color is blue";
        break;

    case 'green':
        echo "The color is green";
        break;
    
    case 'yellow':
        echo "The color is yellow";
        break;

    default:
        echo "The color is not red, blue, green or yellow";
        break;
}